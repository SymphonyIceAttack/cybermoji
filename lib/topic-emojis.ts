export function getTopicBySlug(slug: string): TopicEmojiData | undefined {
  return topicEmojiData.find((topic) => topic.slug === slug);
}

export function isValidTopic(slug: string): boolean {
  return topicEmojiData.some((topic) => topic.slug === slug);
}

export function getAllTopics(): TopicEmojiData[] {
  return topicEmojiData;
}

export function getPopularTopics(limit: number = 6): TopicEmojiData[] {
  return [...topicEmojiData]
    .sort((a, b) => {
      const aMaxPop = Math.max(...a.combinations.map((c) => c.popularity));
      const bMaxPop = Math.max(...b.combinations.map((c) => c.popularity));
      return bMaxPop - aMaxPop;
    })
    .slice(0, limit);
}
